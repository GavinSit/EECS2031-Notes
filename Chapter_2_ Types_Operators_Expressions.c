#include <stdio.h>

/*
Variable Naming
- variables can be characters and numbers
- first character must be letter
- underscore is a letter
- avoid using underscore as first character because lots of library routines
  use it
- upper and lower cases are distinct
*/

/*
Data Types
- char: single byte
- int: integer
	- short: 16-bit, numbers up to 32767
	- long: 32-bit
	- by default compiler chooses automatically
	- e.g. short int count;
- float: single-precision floating point
- double: double-precision floating point
*/

/* 
Escape Sequences
\a alert (bell) character
	- typically used to indicate an error
\b backspace
\f form feed
	- forces printer to eject current page and continue on next page
\n new line
\r carriage return
	- resets device position to beginning of a line of text 
\t horizontal tab
\v vertical tab
\\ backslash
\? question mark
\' single quote
\" double quote
\ooo octal numbers
\xhh hexadecimal number
*/

